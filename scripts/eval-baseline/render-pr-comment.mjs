#!/usr/bin/env node
/**
 * render-pr-comment.mjs (SMI-4764 Wave 3)
 *
 * Reads packages/doc-retrieval-mcp/eval/baseline.json and emits a sticky
 * PR comment to stdout summarising overall + per-category recall@5 deltas.
 *
 * The first line is an HTML comment that acts as the sticky-comment marker:
 *   <!-- skillsmith:eval-baseline-comment -->
 * The CI workflow (Retrieval Eval Gate in .github/workflows/ci.yml) searches
 * for this marker to decide create-vs-update on the PR thread.
 *
 * Usage:
 *   node scripts/eval-baseline/render-pr-comment.mjs
 *   node scripts/eval-baseline/render-pr-comment.mjs --baseline=path/to/baseline.json
 *
 * No dependencies beyond node:fs / node:path.
 */
import { readFileSync, existsSync } from 'node:fs'
import { resolve } from 'node:path'

const STICKY_MARKER = '<!-- skillsmith:eval-baseline-comment -->'
const DEFAULT_BASELINE = 'packages/doc-retrieval-mcp/eval/baseline.json'
const REPRO_CMD =
  'docker exec skillsmith-dev-1 npm run eval:retrieval --workspace=packages/doc-retrieval-mcp -- --real'

/**
 * @param {string[]} argv
 * @returns {string}
 */
function parseBaselineArg(argv) {
  for (const arg of argv) {
    if (arg.startsWith('--baseline=')) {
      return arg.slice('--baseline='.length)
    }
  }
  return DEFAULT_BASELINE
}

/**
 * @param {number | null | undefined} value
 * @returns {string}
 */
function fmtAbs(value) {
  if (value === null || value === undefined || Number.isNaN(value)) return '—'
  return Number(value).toFixed(4)
}

/**
 * @param {number | null | undefined} delta - signed absolute delta
 * @returns {string}
 */
function fmtSignedAbs(delta) {
  if (delta === null || delta === undefined || Number.isNaN(delta)) return '—'
  const sign = delta >= 0 ? '+' : ''
  return `${sign}${delta.toFixed(4)}`
}

/**
 * @param {number | null | undefined} prior
 * @param {number | null | undefined} current
 * @returns {string}
 */
function fmtSignedPct(prior, current) {
  if (
    prior === null ||
    prior === undefined ||
    current === null ||
    current === undefined ||
    Number.isNaN(prior) ||
    Number.isNaN(current)
  ) {
    return '—'
  }
  if (prior === 0) {
    return current === 0 ? '+0.00%' : '—'
  }
  const pct = ((current - prior) / prior) * 100
  const sign = pct >= 0 ? '+' : ''
  return `${sign}${pct.toFixed(2)}%`
}

/**
 * @param {number | null | undefined} prior
 * @param {number | null | undefined} current
 * @returns {number | null}
 */
function computeDelta(prior, current) {
  if (
    prior === null ||
    prior === undefined ||
    current === null ||
    current === undefined ||
    Number.isNaN(prior) ||
    Number.isNaN(current)
  ) {
    return null
  }
  return current - prior
}

/**
 * @param {Record<string, unknown>} baseline
 * @returns {string}
 */
function render(baseline) {
  const metrics = /** @type {Record<string, number>} */ (baseline.metrics ?? {})
  const byCategory = /** @type {Record<string, unknown>} */ (baseline.byCategory ?? {})

  // Overall: prior is a top-level scalar; current is metrics.recallAt5
  const priorOverallRaw = baseline.prior
  const priorOverall = typeof priorOverallRaw === 'number' ? priorOverallRaw : null
  const currentOverall = typeof metrics.recallAt5 === 'number' ? metrics.recallAt5 : null
  const overallDelta = computeDelta(priorOverall, currentOverall)

  const lines = []
  lines.push(STICKY_MARKER)
  lines.push('## 📊 Retrieval Eval — recall@5 deltas')
  lines.push('')
  lines.push(
    `**Overall**: ${fmtAbs(priorOverall)} → ${fmtAbs(currentOverall)} (Δ ${fmtSignedAbs(overallDelta)}, ${fmtSignedPct(priorOverall, currentOverall)})`
  )
  lines.push('')

  // Per-category table
  const recallByCat = /** @type {Record<string, number> | undefined} */ (byCategory.recallAt5)
  const priorByCat = /** @type {Record<string, number> | null | undefined} */ (
    byCategory.recallAt5Prior
  )

  if (recallByCat && typeof recallByCat === 'object') {
    lines.push('| Category | Prior | Current | Δ abs | Δ % |')
    lines.push('|---|---|---|---|---|')
    const categories = Object.keys(recallByCat).sort()
    for (const cat of categories) {
      const current = recallByCat[cat]
      const prior =
        priorByCat && typeof priorByCat === 'object' && cat in priorByCat ? priorByCat[cat] : null
      const delta = computeDelta(prior, current)
      lines.push(
        `| ${cat} | ${fmtAbs(prior)} | ${fmtAbs(current)} | ${fmtSignedAbs(delta)} | ${fmtSignedPct(prior, current)} |`
      )
    }
    lines.push('')
  }

  lines.push(`**Repro locally**: \`${REPRO_CMD}\``)
  lines.push('')
  lines.push('_Generated by `scripts/eval-baseline/render-pr-comment.mjs` (SMI-4764 Wave 3)._')

  return lines.join('\n') + '\n'
}

function main() {
  const baselinePath = resolve(process.cwd(), parseBaselineArg(process.argv.slice(2)))
  if (!existsSync(baselinePath)) {
    process.stderr.write(`render-pr-comment: baseline not found at ${baselinePath}\n`)
    process.exit(1)
  }
  let baseline
  try {
    baseline = JSON.parse(readFileSync(baselinePath, 'utf8'))
  } catch (err) {
    process.stderr.write(
      `render-pr-comment: failed to parse ${baselinePath}: ${err instanceof Error ? err.message : String(err)}\n`
    )
    process.exit(1)
  }
  process.stdout.write(render(baseline))
}

main()
